static void draw_background (cairo_t * cr) {

    int x, y, p=0;
    for(x=0; x<FILAS2;x++){
        for(y=0; y<COLUMNAS2; y++){
            if(map2[x][y]==1){
                cairo_set_source_rgb (cr, 0.0, 0.0, 0.0);
                cairo_rectangle(cr,y*TILESIZE,x*TILESIZE,TILESIZE,TILESIZE);
                cairo_fill (cr);
            }else if(map2[x][y]==2){
                cairo_set_source_rgb (cr, 1.0, 1.0, 0.0);
                cairo_rectangle(cr,y*TILESIZE,x*TILESIZE,TILESIZE,TILESIZE);
                cairo_fill (cr);
            }else if(map2[x][y]==3){
                cairo_set_source_rgb (cr, 0.3, 0.8, 0.0);
                cairo_rectangle(cr,y*TILESIZE,x*TILESIZE,TILESIZE,TILESIZE);
                cairo_fill (cr);
            }else if(map2[x][y]==4){
                cairo_set_source_rgb (cr, 1.0, 1.0, 1.0);
                cairo_rectangle(cr,y*TILESIZE,x*TILESIZE,TILESIZE,TILESIZE);
                cairo_fill (cr);
                
                cairo_set_source_rgb(cr, 0.0, 0.0, 0.0);
                cairo_set_font_size(cr, 13);
                cairo_move_to(cr, y*TILESIZE+6,x*TILESIZE+15);

                cairo_show_text(cr, paradas[p]);
                p++;
            }else if(map2[x][y]==5 || map2[x][y]==6){
                //cairo_set_source_rgb (cr, 0.0, 0.0, 1.0);
                cairo_set_source_rgb (cr, 0.0, 0.0, 0.0);
                cairo_rectangle(cr,y*TILESIZE,x*TILESIZE,TILESIZE,TILESIZE);
                cairo_fill (cr);
            }
        } // for y
    } // for x 
     
} // draw_background

